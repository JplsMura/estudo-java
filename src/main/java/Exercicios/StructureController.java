package Exercicios;

public class StructureController {
    public static void main(String[] args) {
        int number = 2;

        if (number > 0) {
            System.out.println(number  + " é positivo");
        } else if (number < 0) {
            System.out.println(number  + " é negativo");
        } else {
            System.out.println(number  + " é zero");
        }
    }
}
